package Study.Week_7;

public class Calculations {
	public static void main(String[] args) {
		int x = 3;
		int y = 5;

		int sum = x + y;
		int difference = x - y;
		int product = x * y;

		System.out.println("Sum: " + sum);
		System.out.println("Difference: " + difference);
		System.out.println("Product: " + product);
	}
}
